/**
 * TabParamList is a TypeScript type that defines the parameters for each screen
 * in the bottom tab navigator of the application.
 * Each key corresponds to a screen name and the value indicates the parameters
 * that the screen expects. In this case, all screens do not require any parameters.
 */
export type TabParamList = {
  Home: undefined;
  Movies: undefined;
  Search: undefined;
  Series: undefined;
};
